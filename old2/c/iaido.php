<h4>Хранение меча и меры безопасности.</h4>
<h4>Хранение меча.</h4>
&emsp;&emsp;Человек, который использует меч (настоящий или тренировочный) для занятий иайдо, должен в полной мере осознавать свою ответственность за его безопасное хранение.
&emsp;&emsp;Как правило, люди довольно небрежно относятся к вопросу хранения «могито» — тренировочного меча, забывая, что носить меч по улице нужно в специальном чехле, а хранить меч следует только дома.
<h4>Безопасное использование меча.</h4>
&emsp;&emsp;При покупке меча (настоящего или тренировочного) следует выбирать модель с полным набором необходимых принадлежностей – таких как «цуба», «мэкуги» и т.д., на котором есть пометка производителя: «можно использовать для тренировок по иайдо». Также важно учитывать, насколько меч подходит к параметрам тела по весу, длине и т.д.
&emsp;&emsp;При длительном использовании меча, могут возникать поломки из-за некачественной сборки или изнашивания металла, поэтому  следует проводить регулярный осмотр «могито». А после сильного удара о пол или о твердый предмет, нужно разобрать меч, проверить внутреннюю часть и убедиться, что в «хабаки» или «накаго» нет трещин.
<h4>О мерах предосторожности</h4>
&emsp;&emsp;Перед началом тренировки меч следует внимательно осмотреть. Иногда из-за небрежного отношения к проверке происходят  несчастные случаи, поэтому следует всегда соблюдать меры предосторожности, которые описаны ниже.
&emsp;&emsp;При обнаружении повреждения или ослабления крепления, в случае необходимости, нужно заменить деталь или произвести ремонт – при этом лучше обратиться к тренеру или в магазин, где был куплен меч.
&emsp;&emsp;Кроме того, во время тренировки или соревнования нужно передвигаться, контролируя пространство вокруг себя. При перемещении по залу необходимо быть внимательным по отношению к другим людям, а при перемещении на близком расстоянии от другого человека во избежание опасности нанести ему травму следует взять за привычку предупреждать его вслух.
