<h1>Иайдо</h1>
<a href="https://ru.wikipedia.org/wiki/%D0%98%D0%B0%D0%B9%D0%B4%D0%BE" target="_blank">
<img src="/imgs/iaido.jpg"><br></a>
&emsp;&emsp;В переводе с японского иайдо — путь иай; в свою очередь иай — мгновенный удар мечом из ножен, быстрая нейтрализация противника (как правило — убийство) и вкладывание меча обратно в ножны. Иайдока, практикующийся с катаной, стремится контролировать в первую очередь себя, а не своего противника. Искусство иайдо представляет собой изучение и исполнение воображаемых сражений (вадза) без фактического присутствия партнёра. Существуют различные боевые направления, включающие в себя ваза с одним и более воображаемыми оппонентами, а также вариации контактных боёв с использованием тренировочного меча: боккэн, иайто. Каждый бой характерен наличием катаны, изначально скрытой в ножнах. Повышенная концентрация и богатое воображение являются неотъемлемыми характеристиками каждого, стремящегося постичь науку иайдо. Для эффективного ведения боя необходимо соблюдать правильное положение тела, выполнять корректные движения, уметь применять секреты захвата рукоятки катаны и точно наносить режущие удары.
<h4>Этикет</h4>
&emsp;&emsp;Применительно к боевым искусствам («будо») часто говорят: «Все начинается с поклона и заканчивается поклоном». Поклон – это проявление почтения, действие, главный смысл которого заключается в выражении благодарности. На занятиях иайдо большое значение придается как межличностным отношениям  (например, между учителем и учеником, между старшими и младшими учениками), так и  отношению человека к предметам (например, к мечу и  к залу для занятий). Важным при этом является поведение, исполненное чувства уважения.
<br>&emsp;&emsp;Воспринимая боевые искусства («будо») как часть японской культуры, необходимо в полной мере понимать смысл, заложенный в таком действии, как поклон. Во время тренировок соблюдать все традиционные правила: выполнять поклон учителю, поклон залу и мечу. Это делается для того, что бы  сохранить хорошие отношения между людьми.
 
<h4>Что означает «Иайдо».</h4>
&emsp;&emsp;«Иай» означает искусство владения мечом, которое позволяет, почувствовав ярость противника, немедленно отреагировать на  атаку с его стороны и остановить его, молниеносно вынув меч из ножен.
<br>&emsp;&emsp;Занимаясь иайдо, человек учится надлежащим образом владеть своим телом и мечом в различных ситуациях, реагировать на атаку противника из положения сидя, из положения стоя и в движении. Кроме того, занимающийся учится правильному поведению, этикету, и потому иайдо – еще и путь совершенствования своего духа.
 
<h4>История Иайдо.</h4>
&emsp;&emsp;Основателем иайдо считается Хаясидзаки Дзюнсукэ Сигэнобу из местности Осю (современная префектура Ямагата). Его многочисленные ученики создали свои школы, и в эпоху Эдо иайдо получило распространение по всей Японии.  Так называемое «Новое время», когда в Японии  были изданы законы, запрещающие ношение меча, было довольно сложным периодом для боевых искусств, однако благодаря усилиям таких людей как Ооэ Масамити и Накаяма Хакудо, иайдо дожило до наших дней.
 
<h4>Как следует относиться к занятиям.</h4>
&emsp;&emsp;Следует с готовностью и без возражений воспринимать то, что говорит учитель (тренер) и старательно выполнять это на практике. Никакая произвольная интерпретация непозволительна. Также важно заниматься ежедневно (хотя бы понемногу), поскольку умение повышается именно за счет непрерывных занятий, пусть и не продолжительных. Кроме того, огромное значение имеет умение наблюдать за другими («митори-гэйко»), за учителем (тренером), за старшими учениками. Заметив что-либо нужное для себя, следует стремиться перенять это. Таким образом, можно лучше увидеть слабые места в собственной технике, а усилия по восполнению этих слабых мест позволят сделать тренировки гораздо более продуктивными.
 
<h4>Цель обучения иайдо</h4>
&emsp;&emsp;Изначально иайдо возникло как техника владения мечом для защиты и нападения. В наши дни занятия иайдо – это, прежде всего воспитание духа, а физическая подготовка отошла на второй план. При этом нельзя забывать, что основу единства кендо и иайдо («кэнъи-иттай») составляет этикет и техника владение мечом. Есть такое понятие – «нингэн-кэйсэй», оно означает «стремление к полному физическому и духовному раскрытию человека». Исходя из общефилософских вопросов, таких, например, как «Каким должен быть человек?», «Какой должна быть человеческая жизнь?», иайдо помогает раскрыть глубину собственной личности.
 
<h4>Иайдо в рамках Всеяпонской Федерации Кендо.</h4>
<h4>История</h4>
&emsp;&emsp;После окончания второй мировой войны все боевые искусства в Японии были полностью запрещены оккупационными властями. Однако с созданием Всеяпонской федерации кендо в октябре 1952 г., было возрождено кендо, а в 1956 г. и иайдо.
&emsp;&emsp;Для того чтобы выработать и утвердить иайдо ката, которые могли бы практиковать и кендоисты, в федерации было созвано заседание специальной комиссии. В 1969 г. ей были определены семь основных ката: «маэ», «усиро», «укэнагаси», «цукаатэ», «кэсагири», «моротэдзуки», «сампогири». Затем в 1980 г. были добавлены еще три:  «ганмэнатэ», «соэтэдзуки», «сихогири», в 1988 г. эти десять основных ката были официально признаны. И, наконец, в 2000 г. комиссией были добавлены еще два ката: «согири», и «нукиути». Таким образом, на сегодняшний день существует 12 основных ката.
<h4>Современная ситуация.</h4>
&emsp;&emsp;В основе занятий Иайдо, которые проводятся Всеяпонской федерацией кендо, лежат элементы техник всех школ корю (древних боевых искусств). Основу «сэйтэй» иайдо составляют 12 ката, представленные в таблице  ниже. Нужно понимать, что они – только вводная часть, не сложная даже для новичков. Главной целью этих занятий является популяризация иайдо. Ни техника, ни духовная практика ими не исчерпываются. Для более глубокого понимания сути иайдо, важно, чтобы занятия не сводились лишь к отработке ката и не становились занятиями лишь для оценки. Необходимо изучать древние стили, составляющие базу иайдо, хотя для этого и требуется длительная подготовка.
