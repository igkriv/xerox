<h4>Обучение</h4>
&emsp;&emsp;Обучение дзёдо начинается с кихон. Такие приемы как удары хонтэути, гякутэути, хикиотосиути и защитные действия курицукэ и тайатари встречаются во многих ката, поэтому их наработка особенно важна. Простые на первый взгляд, они содержат много тонкостей и требуют точности и четкости исполнения, а также полного контроля над дзё. В зависимости от предыдущей подготовки занимающихся активное внимание может уделяться и работе с мечом, но она в любом случае включается в каждую тренировку как часть разминки.
<br>&emsp;&emsp;Для изучения ката первым делом используют одиночную отработку. Понять точный выбор времени для действия и дистанции в одиночку нельзя, но для запоминания общей последовательности движений это очень удобно. Традиционно, при парном исполнении роль утидати берет на себя учитель или более опытный ученик, поэтому зачастую к изучению этой роли переходят сравнительно поздно. Однако в больших группах начинающих приходится обе роли изучать одновременно.
<h4>Аттестации и соревнования</h4>
&emsp;&emsp;Аттестации по дзёдо проходят в том же виде, что и по иайдо с тем лишь исключением, что ограничения по времени нет. 5 ката выполняются в паре, но оценивается каждый индивидуально. Часто на первый кю достаточно уметь выполнять роль сидати, но начиная с первого дана требуется показать понимание обеих ролей.
<br>&emsp;&emsp;Как и в иайдо, перед сдачей на первый дан нужно успешно пройти аттестацию на первый кю. Время ожидания между экзаменами (а также время до пересдачи в случае неуспеха и некоторые другие общие требования) определяет федерация конкретной страны. Высший уровень данъи (кю и данов) и сёго (преподавательских лицензий) — 8-й дан ханси.
<br>&emsp;&emsp;В соревнованиях-энги в дзёдо участвуют пары. В каждом матче две пары соревнуются между собой, а три судьи определяют, кто, по их мнению, был лучше. На Чемпионатах Европы, однако, соревнования проходят между индивидуальными участниками, поэтому оценивается только партнер с дзё, который может подбирать себе утидати-ассистента среди других участников.
<h5>Необходимое снаряжение</h5>
&emsp;&emsp;Дзёдо не столь требовательно, как иайдо, в плане материальных затрат: хотя иметь подходящую одежду (кэйкоги и хакама) необходимо, нужные для тренировок дзё и боккэн (деревянный меч) стоят заметно меньше, чем качественный иайто.
<br>&emsp;&emsp;Выбирать снаряжение следует очень тщательно. Боккэн и дзё из хорошего дерева, хоть и обойдутся дороже, но прослужат дольше и помогут избежать мелких травм, вызываемых щеплением и заусенцами.
